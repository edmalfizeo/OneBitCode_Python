import statistics

# 1 - Apply the mean function to the list
print(statistics.mean([1, 3, 5, 7, 9]))

# 2 - Apply the median function to the list
print(statistics.median([1, 3, 6, 7, 9]))
print(statistics.median([1, 2, 4, 7, 8, 9]))

# 3 - Apply the mode function to the list
print(statistics.mode([1, 2, 2, 3, 3, 3, 4, 4, 4, 4, 5, 5,]))

# 4 - Apply the stdev function to the list
print(statistics.stdev([1, 1.5, 2, 2.5, 3, 3.5, 4, 4.5, 5, 5.5]))